#include "3-calc.h"

/**
 * op_add - adds two ints
 * Description: adds two ints
 * @a: first int
 * @b: second int
 * Return: result of op
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - substracts two ints
 * Description: substracts two ints
 * @a: first int
 * @b: second int
 * Return: result of op
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two ints
 * Description: multiplies two ints
 * @a: first int
 * @b: second int
 * Return: product of op
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two ints
 * Description: divides two ints
 * @a: first int
 * @b: second int
 * Return: result of op
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets remainder of division
 * Description: gets remainder of division
 * @a: first int
 * @b: second int
 * Return: result of op
 */

int op_mod(int a, int b)
{
	return (a % b);
}
